package loca.bean;

public class LocaDTO {
    private int loca_num;
    private String loca_name;
    private String loca_addr;
    private String loca_phone;
    private String loca_img;
    private String loca_date;
    private String loca_valid;
    
	public String getLoca_valid() {
		return loca_valid;
	}
	public void setLoca_valid(String loca_valid) {
		this.loca_valid = loca_valid;
	}
	public int getLoca_num() {
		return loca_num;
	}
	public void setLoca_num(int loca_num) {
		this.loca_num = loca_num;
	}
	public String getLoca_name() {
		return loca_name;
	}
	public void setLoca_name(String loca_name) {
		this.loca_name = loca_name;
	}
	public String getLoca_addr() {
		return loca_addr;
	}
	public void setLoca_addr(String loca_addr) {
		this.loca_addr = loca_addr;
	}
	public String getLoca_phone() {
		return loca_phone;
	}
	public void setLoca_phone(String loca_phone) {
		this.loca_phone = loca_phone;
	}
	public String getLoca_img() {
		return loca_img;
	}
	public void setLoca_img(String loca_img) {
		this.loca_img = loca_img;
	}
	public String getLoca_date() {
		return loca_date;
	}
	public void setLoca_date(String loca_date) {
		this.loca_date = loca_date;
	}
    
    
}
