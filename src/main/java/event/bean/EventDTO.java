package event.bean;

public class EventDTO {
    private int eve_num;
    private String eve_name;
    private String eve_img;
    private String eve_start;
    private String eve_end;
    private String eve_date;
    
    
	public int getEve_num() {
		return eve_num;
	}
	public void setEve_num(int eve_num) {
		this.eve_num = eve_num;
	}
	public String getEve_name() {
		return eve_name;
	}
	public void setEve_name(String eve_name) {
		this.eve_name = eve_name;
	}
	public String getEve_img() {
		return eve_img;
	}
	public void setEve_img(String eve_img) {
		this.eve_img = eve_img;
	}
	public String getEve_start() {
		return eve_start;
	}
	public void setEve_start(String eve_start) {
		this.eve_start = eve_start;
	}
	public String getEve_end() {
		return eve_end;
	}
	public void setEve_end(String eve_end) {
		this.eve_end = eve_end;
	}
	public String getEve_date() {
		return eve_date;
	}
	public void setEve_date(String eve_date) {
		this.eve_date = eve_date;
	}
    
    
}
